package data;

/**
 * this class describes
 */
class Coordinate {
    private final double x;
    private final double y;

    Coordinate(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public double getY() {
        return y;
    }

    public double getX() {
        return x;
    }
}
